function main() {
  const allDrugs = [
    ...antihypertensives,
    ...diabetesDrugs,
    ...lipidDrugs,
    ...giDrugs,
    ...antithromboticDrugs,
    ...respiratoryDrugs,
    ...allergyDrugs,
    ...painDrugs,
    ...antibiotics,
    ...psychoDrugs,
    ...kampo,
    ...goutDrugs,
    ...thyroidDrugs,
    ...boneDrugs,
    ...vitaminDrugs,
    ...ironDrugs,
    ...urologyDrugs,
    ...miscDrugs,
  ];

  const unique = new Map();
  for (const [name, yj] of allDrugs) {
    if (!unique.has(name)) {
      unique.set(name, yj || "");
    }
  }

  const lines = ["品目名,YJコード,在庫数,月間出庫数"];
  for (const [name, yj] of unique) {
    const monthlyUsage = Math.floor(Math.random() * 196) + 5;
    const daysOfStock = Math.floor(Math.random() * 40) + 2;
    const stock = Math.round(monthlyUsage / 30 * daysOfStock);
    lines.push(`"${name}","${yj}","${stock}","${monthlyUsage}"`);
  }

  const outDir = join(PROJECT_ROOT, "data");
  mkdirSync(outDir, { recursive: true });

  const outPath = join(outDir, "inventory.csv");
  writeFileSync(outPath, "\uFEFF" + lines.join("\n"), "utf-8");

  console.log(`テスト在庫CSVを生成しました: ${outPath}`);
  console.log(`  品目数: ${unique.size}件`);

  const withYJ = [...unique.values()].filter((v) => v).length;
  const withoutYJ = unique.size - withYJ;
  const genericCount = [...unique.keys()].filter(
    (n) => n.includes("「") || n.includes("（医療用）")
  ).length;
  const brandCount = unique.size - genericCount;

  console.log(`  YJコードあり: ${withYJ}件`);
  console.log(`  YJコードなし: ${withoutYJ}件`);
  console.log(`  ジェネリック: ${genericCount}件（${Math.round((genericCount / unique.size) * 100)}%）`);
  console.log(`  先発品: ${brandCount}件（${Math.round((brandCount / unique.size) * 100)}%）`);
  console.log("");
  console.log("インポートするには:");
  console.log("  node tools/import-csv.mjs data/inventory.csv --replace");
}

main();
